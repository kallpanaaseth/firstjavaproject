package com.company;

public class Whileloop {
    public static void main(String[] args) {

        int j = 1;
        while (j <= 10) {
            System.out.println(j);
            j++;
        }


        int k = 1;
        do {
            System.out.println(k);
            k++;
        } while (k <= 10);
    }
}
